						<h2><b>Bagaimana Menjalankan <i>Task Scheduler</i>?</b></h2>
						<p align="justify">
						Ada dua konsep kunci untuk menjalankan <i>Task Scheduler</i> dalam penjadwalan yaitu <b><i>action</i></b> dan <b><i>triggers</i></b>. Sebuah <b><i>triggers</i></b> menyebabkan "Tugas" atau "<i>Task</i>" untuk berjalan, dan <b><i>action</i></b> adalah pekerjaan yang dilakukan ketika "Tugas" atau "<i>Task</i>" dijalankan. <b><i>action</i></b> saat melakukan tugasnya dapat mencakup untuk menjalankan program, mengirim pesan e-mail, dan menunjukkan kotak pesan. Misalnya, Anda dapat mengirim e-mail saat entri acara tertentu dicatat dalam log peristiwa atau menjalankan <i>script-script</i> sebuah program. Dalam <b><i>triggers</i></b> anda dapat menjadwalkan tugas untuk dijalankan pada waktu-tertentu tertentu.
						</p>
